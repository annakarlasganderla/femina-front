


export const setBackgroundColor = (backgroundColor: string): string => {
  switch (backgroundColor) {
    case "primary":
      return "#9B4A46";
    case "secondary":
      return "#fff";
    default:
      return "#9B4A46";
  }
};

export const setThemeColor = (themeColor: string): string => {
  switch (themeColor) {
    case "primary":
      return "#9B4A46";
    case "secondary":
      return "#fff";
    default:
      return "#9B4A46";
  }
};
